# Python3 round() 函数

"""
描述
round() 方法返回浮点数x的四舍五入值。

语法
以下是 round() 方法的语法:

round( x [, n]  )
参数
x -- 数值表达式。
n -- 数值表达式。
返回值
返回浮点数x的四舍五入值。
实例
以下展示了使用 round() 方法的实例：
"""

print("round(70.23456) : ", round(70.23456))
print("round(56.659,1) : ", round(56.659, 1))
print("round(80.264, 2) : ", round(80.264, 2))
print("round(100.000056, 3) : ", round(100.000056, 3))
print("round(-100.000056, 3) : ", round(-100.000056, 3))




